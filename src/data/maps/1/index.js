let enemies = []
for (var i = 0; i < 10; i ++){
  enemies.push(
    {
      type: 'spider',
      health: 50,
      attack: 10,
    }
  )
}
function createNewTiles(tileset){
  let tiles = tileset
  for (var i = 0; i < 20; i++){
    for (var j = 0; j < 12; j++){
      if(tiles[j][i] === 0){
        while(enemies.length > 0){
          tiles[Math.floor((Math.random() * 11))][Math.floor((Math.random() * 19))] = enemies.pop()
        }
      }
    }
  }
  return tiles
}

